export const KNOWLEDGE: Record<RepId, RepKnowledge> = { white: WHITE, black: BLACK };

/** One concise Hebrew sentence: what this move tries to do in the opening. */
export function explainMove(
  repId: RepId,
  node: { san: string | null; playedBy: 'w' | 'b' | null },
): string {
  if (node.san === null) return '';
  const k = KNOWLEDGE[repId];
  const isHero = node.playedBy === k.hero;
  const table = isHero ? k.heroIdeas : k.oppIdeas;
  return table[node.san] ?? (isHero ? k.heroFallback : k.oppFallback);
}
